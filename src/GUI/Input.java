    private String pegaAutomato(String arqName) throws IOException {
        BufferedReader myBuffer = new BufferedReader(new InputStreamReader(new FileInputStream(arqName), "UTF-8"));
        String linha = myBuffer.readLine();
        String comando = "";

        while (linha != null) {
            comando += linha;
            linha = myBuffer.readLine();
        }
        return comando;
    }

