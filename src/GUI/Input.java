    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String arq = jTextField1.getText();
        if (arq != null && !arq.equals("")) {
            String path = System.getProperty("user.dir") + "/" + arq;
            try {
                String comando = pegaAutomato(path);
                System.out.println(comando);
                ANTLRInputStream input = new ANTLRInputStream(comando);
                automatoLexer lexer = new automatoLexer(input);
                CommonTokenStream tokens = new CommonTokenStream(lexer);
                automatoParser parser = new automatoParser(tokens);
                ParseTree tree = parser.exprAutomato();
                ParseTreeWalker walker = new ParseTreeWalker();
                ShortToUnicodeString s = new ShortToUnicodeString();
                walker.walk(s, tree);
                System.out.println();

                ArrayList<String> terminais = s.getTerminais();
                if (terminais != null) {
                    this.t = new TerminaisOrganizados(terminais);
                    if (t.isAfnd()) {
                        jLabel2.setText("Erro: O autômato é não determinístico!");
                        jDialog1.setVisible(true);
                        return;
                    }
                    if (t.isErroAlfabeto()) {
                        jLabel2.setText("Erro: Caractere não contido no alfabeto de entrada!");
                        jDialog1.setVisible(true);
                        return;
                    }
                    if (t.isErroEstado()) {
                        jLabel2.setText("Erro: Estado não contido na lista de estados!");
                        jDialog1.setVisible(true);
                        return;
                    }
                    if (t.isErroEstadoInicial()) {
                        jLabel2.setText("Erro: Transicão sem o estado inicial informado!");
                        jDialog1.setVisible(true);
                        return;
                    }
                    if (t.isErroEstadoFinal()) {
                        jLabel2.setText("Erro: Transições nunca levam a um estado final!");
                        jDialog1.setVisible(true);
                        return;
                    }
                    jButton3.setEnabled(true);
                    jTextField2.setEditable(true);
                    jButton1.setEnabled(false);
                    jTextField1.setEditable(false);
                } else {
                    jLabel2.setText("Erro na sintaxe do automato!");
                    jDialog1.setVisible(true);
                    return;
                }
            } catch (IOException ex) {
                Logger.getLogger(Input.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

